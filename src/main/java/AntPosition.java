public class AntPosition {


    private final int antX;
    private final int antY;

    public AntPosition(int antX, int antY) {

        this.antX = antX;
        this.antY = antY;
    }

    public int getXCood() {
        return antX;
    }

    public int getYCood() {
        return antY;
    }
}
